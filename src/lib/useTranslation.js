export function useTranslation() {
  const { lang } = useLanguage();

  const t = (key) => {
    return DICTIONARY[lang]?.[key] || DICTIONARY['en']?.[key] || key;
  };

  const tField = (obj, field) => {
    if (!obj) return '';
    if (lang === 'ms' && obj[`${field}My`]) {
      return obj[`${field}My`];
    }
    return obj[field] || '';
  };

  return { t, tField, lang };
}
